    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jPanel1.removeAll();
        try{
            size=Integer.parseInt(jTextField2.getText().trim());
            if(size<=0){
                jTextField2.setText("invalid grid size.");
                return;
            }
            jPanel1.setLayout(new GridLayout(size, size));
            cell=new JTextField[size][size];
            inputArray=new String[size][size];
            for(int i=0; i<size; i++){
                for(int j=0; j<size; j++){
                    inputArray[i][j]=Integer.toString((i+j)%size+1);
                }
            }
            for(int i=0; i<size*size/2; i++) {
                inputArray[(int)(Math.random()*size)][(int)(Math.random()*size)]="";
            }
            for(int i=0; i<size; i++) {
                for(int j=0; j<size; j++) {
                    cell[i][j]=new JTextField();
                    if(!inputArray[i][j].equals("")) {
                        cell[i][j].setText(inputArray[i][j]);
                        cell[i][j].setEditable(false);
                        cell[i][j].setBackground(Color.pink);
                    }
                    jPanel1.add(cell[i][j]);
                }
            }
            jPanel1.validate();
            jPanel1.repaint();
        } 
        catch(NumberFormatException ex){
            jTextField2.setText("enter a valid number.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

