    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:                                         
        boolean valid=true;
        int[][] values=new int[size][size];
        for (int i=0; i<size; i++){
            for (int j=0; j<size; j++){
                String text=cell[i][j].getText().trim();
                if(text.equals("") || !text.matches("\\d+")){
                    cell[i][j].setBackground(Color.red);
                    valid=false;
                } 
                else{
                    int val=Integer.parseInt(text);
                    if(val<=0||val>size){
                        cell[i][j].setBackground(Color.red);
                        valid=false;
                    } 
                    else{
                        if(cell[i][j].isEditable()){
                            cell[i][j].setBackground(Color.white);
                        }
                        values[i][j]=val;
                    }
                }
            }
        }
        for(int i=0; i<size; i++){
            boolean[] rowCheck=new boolean[size+1];
            boolean[] colCheck=new boolean[size+1];
            for (int j=0; j<size; j++){
                int rowVal=values[i][j];
                int colVal=values[j][i];
                if(rowCheck[rowVal]){
                    valid=false;
                    cell[i][j].setBackground(Color.orange);
                }
                else{
                    rowCheck[rowVal]=true;
                }
                if(colCheck[colVal]){
                    valid=false;
                    cell[j][i].setBackground(Color.orange);
                }
                else{
                    colCheck[colVal]=true;
                }
            }
        }
        if(valid){
            jTextField1.setText("Congrats! Valid Sudoku.");
        } else {
            jTextField1.setText("Invalid Sudoku entries!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

