    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        text=jTextArea1.getText();// TODO add your handling code here:
        if(text.length()==0){ 
            jTextField1.setText("0");
        }
        else {
            jTextField1.setText(Integer.toString(text.split("\n", -1).length));
        }
        //System.out.println(text.split("\n", -1));
        jTextField2.setText(Integer.toString(text.split("\s", -1).length-1));
        jTextField3.setText(Integer.toString(text.split("\t", -1).length-1));
        jTextField4.setText(Integer.toString(text.length()));
        arr=text.split("[\n\t\s]",-1);
        words=0;
        s1="";
        s2="";
        for(String i: arr){
            if(!i.equals("")){
                words+=1;
            }
            s1+=i;
        }
        jTextField5.setText(Integer.toString(words));
        jComboBox1.removeAllItems();// TODO add your handling code here:
        for(int i=0; i<s1.length(); i++){
            if(!s2.contains(Character.toString(s1.charAt(i)))){
                s2+=s1.charAt(i);
                jComboBox1.addItem(Character.toString(s1.charAt(i)));
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

