    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        int index = jComboBox1.getSelectedIndex();
        text = jTextArea1.getText();
        if(index != -1 && index < jComboBox1.getItemCount()){
            String letter= (String) jComboBox1.getItemAt(index);
            if(letter!= null && letter.length() > 0) {
                char charToCount = letter.charAt(0);
                int length = text.split(java.util.regex.Pattern.quote(String.valueOf(charToCount)), -1).length - 1;
                jTextField6.setText(length + "");
            } 
            else{
                jTextField6.setText("0");
            }
        } 
        else {
            jTextField6.setText("0");
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

